    private void lblAddLearnerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAddLearnerMouseClicked
        // TODO add your handling code here:
        if (txtLearnerID.getText().length() <= 0 || txtLearnerID.getText().length() > 7) {
            MsgBox.alert(this, "ID invalid, ID length must betweens 0 and 7 ");
        }
        if (learnerdao.selectByID(txtLearnerID.getText()) != null)
            MsgBox.alert(this, "ID Duplicate");
        else if (txtLearnerEmail.getText().equals("") || txtLearnerBirth.getText().equals("")
                || txtLearnerID.getText().equals("") || txtLearnerName.getText().equals("")
                || txtLearnerPhone.getText().equals("")) {
            MsgBox.alert(this, "You must entered all informations");
        } else {
            Learner learner = getFormLearner();
            learnerdao.insert(learner);
            MsgBox.alert(this, "Insert successfully");
            openLearner();
            clearLearnerForm();
        }
    }//GEN-LAST:event_lblAddLearnerMouseClicked

