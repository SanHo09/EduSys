    void fillTableStudent() {
        DefaultTableModel model = (DefaultTableModel)tblStudent.getModel();
        model.setRowCount(0);
        Course course = (Course)cboStudentCourse.getSelectedItem();
        if(course!=null) {
            List<Student> list = studentdao.selectByCourse(course.getCourseID());
            for(int i=0;i<list.size();i++) {
                Student student = list.get(i);
                String name = learnerdao.selectByID(student.getLearnerID()).getName();
                model.addRow(new Object[] {
                    i+1,
                    student.getStudentID(),
                    student.getLearnerID(),
                    name,
                    student.getMark()
                });
            }
            this.fillTableLearner();
        }
    }
    
