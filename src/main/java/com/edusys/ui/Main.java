    private void btnUpdateMarkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateMarkActionPerformed
        // TODO add your handling code here:
        
        for(int i=0;i<tblStudent.getRowCount();i++) {
            if(Double.parseDouble(String.valueOf(tblStudent.getValueAt(i, 4)))<0||Double.parseDouble(String.valueOf(tblStudent.getValueAt(i, 4)))>10) {
                MsgBox.alert(this, "Mark must be between 0 and 10");
                return;
            }else {
                double newMark = Double.parseDouble(String.valueOf(tblStudent.getValueAt(i, 4)));
                int StudentID = (Integer)tblStudent.getValueAt(i, 1);
                Student student = studentdao.selectByID(String.valueOf(StudentID));
                Double mark = student.getMark();
                student.setMark(newMark);
                studentdao.update(student);
                Learner learner = learnerdao.selectByID(student.getLearnerID());
                if(mark==0||newMark!=mark) {
                    String subject = String.valueOf(cboStudentCourse.getSelectedItem());
                    Thread t = new Thread(){
                      @Override
                      public void run() {
                          Xmail.sendMail(learner.getEmail(), "subject score sheet", "Your mark in the subject:  "+subject+"is :"+"  " + newMark);
                      }
                    };
                    t.start();
                    
                }
            }
        }
        MsgBox.alert(this, "Update Successfully");
    }//GEN-LAST:event_btnUpdateMarkActionPerformed

