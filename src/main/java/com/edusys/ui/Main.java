    private void txtSearchLearnerKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchLearnerKeyReleased
        // TODO add your handling code here:
        List<Learner> list = learnerdao.selectAll();
        DefaultTableModel model = (DefaultTableModel) tblLearner.getModel();
        model.setRowCount(0);
        try {
            for (int i = 0; i < list.size(); i++) {
                if (list.get(i).getLearnerID().contains(txtSearchLearner.getText())
                        || list.get(i).getName().contains(txtSearchLearner.getText())
                        || list.get(i).getPhoneNumber().contains(txtSearchLearner.getText())
                        || list.get(i).getStaffID().contains(txtSearchLearner.getText())
                        || list.get(i).getEmail().contains(txtSearchLearner.getText())) {
                    Object[] row = {
                        list.get(i).getLearnerID(),
                        list.get(i).getName(),
                        list.get(i).getGender() ? "Male" : "Female",
                        list.get(i).getBirth(),
                        list.get(i).getPhoneNumber(),
                        list.get(i).getEmail(),
                        list.get(i).getLearnerDescription(),
                        list.get(i).getStaffID(),
                        list.get(i).getRegistDate(),
                        list.get(i).getPicture()
                    };
                    model.addRow(row);
                }
            }
//            
        } catch (Exception ex) {
            MsgBox.alert(this, "Data Error");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_txtSearchLearnerKeyReleased

