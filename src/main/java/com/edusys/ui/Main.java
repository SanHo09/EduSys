    private void btnDelete_ThematicMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDelete_ThematicMouseClicked
        // TODO add your handling code here:
        if(!Auth.isManager()) 
            MsgBox.alert(this, "you can not delete");
        else {
            try {
                if (MsgBox.confirm(this, "You want to delete this Thematic ?")) {
                    thematicdao.delete(thematicdao.selectByID(txtThematicID.getText()));
                    MsgBox.alert(this, "Delete Successfully");
                    clearThematicForm();
                    openThematic();
                }
            } catch (Exception ex) {
                MsgBox.alert(this, "This Thematic has a course, cannot be deleted");
            }
        }
    }//GEN-LAST:event_btnDelete_ThematicMouseClicked

    private void btnAdd_ThematicMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAdd_ThematicMouseClicked
        // TODO add your handling code here:

        if (txtThematicID.getText().length() == 0 || txtThematicID.getText().length() > 5) {
            MsgBox.alert(this, "ID invalid, ID must betweens 0 and 5");
        } else if (txtThematicName.getText().length() <= 0 || txtTuition.getText().length() <= 0
                || txtThematicTime.getText().length() <= 0 || txtThematicDescription.getText().length() <= 0) {
            MsgBox.alert(this, "You must enter all the information !");
        } else {
            Thematic thematic = getFormThematic();
            Thematic check = thematicdao.selectByID(txtThematicID.getText());
            if (check != null) {
                MsgBox.alert(this, "ID duplicated !");
                return;
            } else {
                MsgBox.alert(this, "Insert successfully !");
                if (thematic.getPicture() == null) {
                    thematic.setPicture("No_image.jpg");
                }
                thematicdao.insert(thematic);
                openThematic();
            }

        }
    }//GEN-LAST:event_btnAdd_ThematicMouseClicked

